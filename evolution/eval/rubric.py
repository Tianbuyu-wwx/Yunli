def get_dimension_names() -> List[str]:
    """获取所有维度名称（按权重排序）"""
    sorted_dims = sorted(RUBRIC.items(), key=lambda x: x[1]["weight"], reverse=True)
    return [dim[0] for dim in sorted_dims]


def get_max_possible_score() -> float:
    """获取理论满分（所有维度满分 10，归一化到 100 分制）"""
    return 100.0


def get_dimension_weight(dim_name: str) -> float:
    """获取维度权重"""
    return RUBRIC[dim_name]["weight"]